	<h1><center>O Fundacji</center></h1>
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Fundacja Polskiego Filmu Historycznego została powołana, aby wesprzeć  finansowo scenariusze filmowe, które mają trudności z pozyskaniem dofinansowania z instytucji państwowych. Dotyczy to w szczególności tematyki lat  1939-1956.<br/><br/>
&nbsp;&nbsp;&nbsp;&nbsp;Fundacja chce przywrócić pamięć Powstania Styczniowego, w  szczególności jego dyktatora Romualda Traugutta , bohaterów ziem kresowych: Emilii  Plater i Ludwika Narbutta oraz partii leśnych.
Druga Wojna Światowa , państwo Polskie będąc pod okupacją Niemiecką wystawia czwartą co do wielkości armię koalicji antyhitlerowskiej, jej heroiczne, bohaterskie zmagania na frontach drugiej wojny światowej oraz w  kraju nie są znane opinii światowej.<br/>
Polscy lotnicy w bitwie o Anglię. Armia gen. Władysława Andersa, jej szlak bojowy od utworzenia do końca wojny. Dzieje dywizji pancernej gen. Franciszka Maczka. Losy Cichociemnych elity dowódczej Polskiego Państwa Podziemnego. Fascynujące   losy zgrupowań partyzanckich , Jana Piwnika 'Ponury', Adolfa Pilcha 'Góra-Dolina' czy Brygady Świętokrzyskiej.<br/>
Wreszcie bohaterowie okupacji sowieckiej, żołnierze Wyklęci – Niezłomni: Henryk Flame - 'Bartek', Antoni Heda – 'Szary', Stanisław Sojczyński – 'Warszyc', Zygmunt Szendzielasz - 'Łupaszka', Antoni Radziwonik – 'Olech', Józef Kuraś – 'Ogień', Hieronim Dekutowski – 'Zapora', Marian Bernaciak – 'Orlik' zgrupowanie pod jego dowództwem 24 maja 1945 stoczyło w Lesie Stockim koło Kazimierza Dolnego od zakończenia wojny największą, zwycięską bitwę z wojskami NKWD i UB. Takich bohaterów było setki, tysiące. Oddali życie za wolną Polskę. Współczesna młodzież musi poznać losy tych bohaterskich żołnierzy, dowódców którzy walcząc z okupacją niemiecką i sowiecką byli w ich wieku.<br/>
Należy budować prestiż Polski odwołując się do pięknych kart z jej historii na które głucha jest Europa i Świat. Młode pokolenie należy edukować przypominając wspaniałe losy naszego oręża od początku istnienia naszej państwowości do czasów współczesnych.<br/><br/>
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Realizacja jakiegokolwiek scenariusza filmowego będzie zależała od Państwa hojności. Wspólnym wysiłkiem jesteśmy w stanie zrealizować wiele projektów.
